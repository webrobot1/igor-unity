using Newtonsoft.Json.Linq;
using System;
using System.Collections.Generic;

namespace Mmogick
{
    public class EventRecive
    {
        public string action = null;

        /// <summary>
        /// сколько осталось до таймаута секунд .я не рассылаю точное время тк 100% на клиенте и сервере время не совпадет и будет рассинхрон пару секунд. используется для сверки когда отправлять пакет можно 
        /// </summary>
        public double? remain = null;

        /// <summary>
        /// сколько секуд таймаут события. приходит при загрузке или если в процессе игры изменился на группу события. мы не отнимаем ping (если непрерывное событие это время понадобится для отправки серверу нового пакета) так что используем для анимаций
        /// </summary>
        public double? timeout = null;

        public JObject data;

        /// <summary>
        /// Источник события. Сервер шлёт только false, true никогда не приходит (избыточно):
        /// false — событие создано сервером (код механик). Клиент может прервать его новой командой
        ///         независимо от action и remain. Пример: move/walk/to — первый вызов от клиента,
        ///         далее сервер повторяет каждую клетку пути (false) — игрок может прервать
        ///         и пойти в другую сторону.
        /// null  — поле отсутствует в пакете. Два случая:
        ///         1) событие создано клиентом (сервер не шлёт from_client — клиент и так знает)
        ///         2) событие завершилось (action="", сервер не включает from_client)
        /// Локально на клиенте from_client = true ставится после Send() для блокировки повторной отправки.
        /// </summary>
        public bool? from_client = null;
    }
}
